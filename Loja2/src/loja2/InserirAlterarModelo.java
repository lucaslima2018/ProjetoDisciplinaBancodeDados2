    public void consultar() {
        String query;
        try {
            query = "SELECT * FROM MODELO";

            Statement st = (Statement) con.createStatement();

            ResultSet rs = st.executeQuery(query);
            if (rs != null) {
                while (rs.next()) {

                    int codigo = rs.getInt("CODIGO");
                    String nome = rs.getString("NOME");
                    

                    model.addRow(new Object[]{codigo, nome});

                }
            }
        } catch (Exception e) {
        }
    }
    
    
    
    public void cadastrarModelo(String nome) {
        try { 
            String query = "INSERT INTO MODELO (NOME)"
                    + "values ('" + nome + "')";
            Statement st = (Statement) con.createStatement();
            int resultado = st.executeUpdate(query);
            if (resultado == 1) {
                JOptionPane.showMessageDialog(this, "Modelo cadastrado com sucesso");
            }
            System.out.println(resultado);
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(this, e);
            System.out.println(e);
        }
    }
    
    
    public void excluirModelo(int codigo) {
        try {
            this.limpaTable();
            String menssagem = "Deseja realmente excluir o modelo?";
            String titulo = "Excluir Modelo";

            int opcao = JOptionPane.showConfirmDialog(null, menssagem, titulo, JOptionPane.YES_NO_OPTION);

            String query = "DELETE FROM MODELO WHERE CODIGO=" + codigo;
            Statement st = (Statement) con.createStatement();
            int resultado = st.executeUpdate(query);

            if (opcao == JOptionPane.YES_OPTION) {
                if (resultado == 1) {
                    JOptionPane.showMessageDialog(null, "Modelo excluído com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir modelo.");
                }
            }else {
                return;
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
    
    
    public void limpaTable() {
        for (int linha = model.getRowCount() - 1; linha >= 0; linha--) {
            model.removeRow(linha);
        }
    }
    
    
    
    
    
    
